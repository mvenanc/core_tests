namespace exercicio_03 {
    public class Triangulo {
        public double A { get;  set; }
        public double B { get;  set; }
        public double C { get;  set; }

        public Triangulo (double a, double b, double c) {
            this.A = a;
            this.B = b;
            this.C = c;
        }        
    }
}